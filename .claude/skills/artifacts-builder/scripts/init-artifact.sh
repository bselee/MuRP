#!/bin/bash
# init-artifact.sh - Initialize a React artifact project with shadcn/ui
# Usage: bash init-artifact.sh <project-name>

set -e

PROJECT_NAME="${1:-artifact}"

if [ -z "$1" ]; then
    echo "Usage: bash init-artifact.sh <project-name>"
    echo "Example: bash init-artifact.sh my-dashboard"
    exit 1
fi

echo "Creating artifact project: $PROJECT_NAME"

# Create project directory
mkdir -p "$PROJECT_NAME"
cd "$PROJECT_NAME"

# Detect Node version for Vite compatibility
NODE_VERSION=$(node -v | cut -d'v' -f2 | cut -d'.' -f1)
if [ "$NODE_VERSION" -lt 18 ]; then
    echo "Warning: Node.js 18+ recommended. Current: $(node -v)"
    VITE_VERSION="4.5.0"
else
    VITE_VERSION="5.4.2"
fi

